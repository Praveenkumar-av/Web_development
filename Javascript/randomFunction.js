// Random Function in Javascript
// Math.random() function

let num = Math.random(); // creates a random number between 0 and 1
console.log(num);
num = Math.random()*10; // creates a random number from 0 to 9.999999999999999
console.log(num);
num = Math.floor(Math.random()*10); // creates a random number from 0 to 9
console.log(num);
num = Math.floor(Math.random()*10)+1; // creates a random number from 1 to 10
console.log(num);