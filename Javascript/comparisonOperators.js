// Comparison operators in javascript

const x = 10;
const y = '10';
const z = 5;
 
// == (loose equality) - compares only values
console.log("x == y:", x == y);

// === (strict equality) - compares values and types
console.log("x === y:", x === y);

// != (loose inequality) - true if values differ
console.log("x != y:", x != y);

// !== (strict inequality) - checks type + value
console.log("x !== y:", x !== y);

// > (greater than)
console.log("x > z:", x > z);

// >= (greater or equal)
console.log("x >= z:", x >= z);

// < (less than)
console.log("x < z:", x < z);

// <= (less or equal)
console.log("x <= z:", x <= z);
